export type Firefighter = {
  id: string; // Firestore document ID (autogenerated, immutable)
  legajo: string;
  firstName: string;
  lastName: string;
  rank:
    | 'ASPIRANTE'
    | 'BOMBERO'
    | 'CABO'
    | 'CABO PRIMERO'
    | 'SARGENTO'
    | 'SARGENTO PRIMERO'
    | 'SUBOFICIAL PRINCIPAL'
    | 'SUBOFICIAL MAYOR'
    | 'OFICIAL AYUDANTE'
    | 'OFICIAL INSPECTOR'
    | 'OFICIAL PRINCIPAL'
    | 'SUBCOMANDANTE'
    | 'COMANDANTE'
    | 'COMANDANTE MAYOR'
    | 'COMANDANTE GENERAL';
  firehouse: string;
  status: 'Active' | 'Inactive';
};

export type AttendanceStatus = "present" | "absent" | "tardy" | "excused";

export type Session = {
  id: string;
  title: string;
  description: string;
  specialization: 'APH' | 'BUCEO' | 'FORESTAL' | 'FUEGO' | 'GORA' | 'HAZ-MAT' | 'KAIZEN' | 'PAE' | 'RESCATE' | 'VARIOS';
  date: string;
  startTime: string;
  instructors: Firefighter[];
  assistants: Firefighter[];
  attendees: Firefighter[];
  attendance?: Record<string, AttendanceStatus>; // Firefighter ID -> Status
};

export type LeaveType = 'Ordinaria' | 'Extraordinaria' | 'Sanción' | 'Enfermedad' | 'Estudio';

export type Leave = {
  id: string;
  firefighterId: string; // The immutable Firestore document ID
  firefighterName: string; // Storing for easier display
  startDate: string;
  endDate: string;
  type: LeaveType;
};

export type UserRole = 'Administrador' | 'Operador' | 'Ayudantía';

export type User = {
  id: string;
  name: string;
  password: string;
  role: UserRole;
};

// Tipo para el usuario logueado, puede ser nulo si no está autenticado
export type LoggedInUser = Omit<User, 'password'> | null;
